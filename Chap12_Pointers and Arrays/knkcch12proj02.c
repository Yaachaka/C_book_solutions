/*
@@@@ PROGRAM NAME: knkcch12proj02.c
@@@@ FLAGS: -std=c99
@@@@ PROGRAM STATEMENT: (a) Write a program that reads a message. then checks 
whether it’s a palindrome (the letters in the message are the same from left to right 
as from right to left):

	Enter a message: He lived as a devil, eh?
	Palindrome
	
	Enter a message: Madam, I am Adam.
	Not a palindrome

Ignore all characters that aren’t letters. Use integer variables to keep track of 
positions in the array.
(b) Revise the program to use pointers instead of integers to keep track of positions 
in the array.
*/
#include<stdio.h>
#include<ctype.h>  //tolower(), isalpha()
#include<stdbool.h>  //bool, true, false
//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
//------------------------START OF MAIN()--------------------------------------
int main(void)
{
	printf("++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++\n");
	
	const short n=80;
	char a[n],ch,*p=a;
	bool palindrome=true;
	
	//Getting meesage from user.
	printf("Enter a message: ");
	while((ch=tolower(getchar()))!='\n' && p<a+n)
		if(isalpha(ch))
			*p++=ch;
	
	char *i=a;
	while(--p>i)
		if(*p!=*i++)
		{
			palindrome=false;
			break;
		}
	
	if(palindrome)
		printf("Palindrome.");
	else
		printf("Not a palindrome.");

	printf("\n++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++\n");
	return 0;
}
//-------------------------END OF MAIN()---------------------------------------
