#include<stdio.h>
#include<ctype.h>  //tolower(), isalpha()
#include<stdbool.h>  //bool, true, false
//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
//------------------------START OF MAIN()--------------------------------------
int main(void)
{
	printf("++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++\n");
	
	const short n=80;
	char a[n],ch;
	short i=0,j=0;
	bool palindrome=true;
	
	//Getting meesage from user.
	printf("Enter a message: ");
	while((ch=tolower(getchar()))!='\n' && i<n)
		if(isalpha(ch))
			*(a+i++)=ch;
	
	while(--i>j)
		if(*(a+i)!=*(a+j++))
		{
			palindrome=false;
			break;
		}
	
	if(palindrome)
		printf("Palindrome.");
	else
		printf("Not a palindrome.");


	printf("\n++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++\n");
	return 0;
}
//-------------------------END OF MAIN()---------------------------------------
