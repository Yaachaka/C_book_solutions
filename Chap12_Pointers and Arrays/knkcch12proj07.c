int main(void)
{
	printf("++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++\n");
	
	int b[N], i, big, small;

	printf("Enter %d numbers: ", N);
	for (i = 0; i < N; i++)
		scanf("%d", &b[i]);

	max_min(b, N, &big, &small);

	printf("Largest: %d\n", big);
	printf("Smallest: %d", small);


	printf("\n++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++\n");
	return 0;
}
//-------------------------END OF MAIN()---------------------------------------
void max_min(int *a, int n, int *max, int *min)
{
	int i;

	*max = *min = *a;
	for (i = 1; i < n; i++)
	{
		if (*(a+i) > *max)
			*max = *(a+i);
		else if (*(a+i) < *min)
			*min = *(a+i);
	}
}
