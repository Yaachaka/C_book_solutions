//------------------------START OF MAIN()--------------------------------------
int main(void)
{
	printf("++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++\n");
	
	const int n=10;
	char a[n];

	printf("Enter a test statement: ");
	gets(a);

	censor(a,n);
	printf("The output statement: %s",a);


	printf("\n++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++\n");
	return 0;
}
//-------------------------END OF MAIN()---------------------------------------
void censor(char a[],int n)
{
	int i=0;

	while(a[i+2]!='\0' && i<n-2)
	{
		if(tolower(a[i])=='f' && tolower(a[i+1])=='o' && tolower(a[i+2])=='o')
		{
			a[i++]='x';
			a[i++]='x';
			a[i++]='x';
		}
		else
			i++;
	}
}
