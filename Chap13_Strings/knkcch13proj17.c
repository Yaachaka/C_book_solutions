bool is_palindrome(const char *message)
{
	char a[80],ch,*p=a,*q=a;
	
	strcpy(a,message);

	while(ch=tolower(*message++))
		if(isalpha(ch))
			*p++=ch;
	
	*p='\0';
	while(--p>q)
		if(*p!=*q++)
			return false;
	
	return true;
	
}
