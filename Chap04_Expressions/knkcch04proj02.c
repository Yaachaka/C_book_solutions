#include<stdio.h>
//------------------------START OF MAIN()--------------------------------------
int main(void)
{
	printf("++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++\n");
	
	int n;
	
	//User input
	printf("Enter a three-digit number: ");
	scanf("%3d",&n);
	
	//Output
	printf("The reversal is: %d.",(n%10)*100+((n%100)/10)*10+n/100);
	
	printf("\n++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++\n");
	return 0;
}
//-------------------------END OF MAIN()---------------------------------------
