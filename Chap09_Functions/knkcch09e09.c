#include<stdio.h>
//---------------------------------------------------------------------------
void swap(int a, int b);
//------------------------START OF MAIN()--------------------------------------
int main(void)
{
	printf("++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++\n");
	
	int i=1, j = 2;
	
	swap(i, j);
	printf("i = %d, j = %d\n", i, j);
	
	printf("\n++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++\n");
	return 0;
}
//-------------------------END OF MAIN()---------------------------------------
void swap(int a, int b)
{
	int temp = a;
	a= b;
	b = temp;
}
